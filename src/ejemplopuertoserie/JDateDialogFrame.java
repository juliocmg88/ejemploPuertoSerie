    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String initialDate = sdf.format(jDateChooser1.getDate());
        String finalDate = null;
        JTablaEventos table = null;
        if (jRadioButton2.isSelected()) {
            try {
                Calendar cal = Calendar.getInstance();
                cal.setTime(jDateChooser2.getDate());
                cal.add(Calendar.DATE, 1);
                finalDate = sdf.format(cal.getTime());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Coloque un valor v√°lido en el campo Fecha Final",
                        "Advertencia",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        }
        if (finalDate != null
                && jDateChooser2.getDate().compareTo(jDateChooser1.getDate()) < 0) {
            JOptionPane.showMessageDialog(this,
                    "La Fecha Final debe ser mayor que la Fecha Inicial",
                    "Advertencia",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            table = new JTablaEventos(serialPortManagement, initialDate, finalDate);
            table.setLocation(getX() + 400, getY() + 250);
            table.setVisible(true);
            table.setAlwaysOnTop(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

